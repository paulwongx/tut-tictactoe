function calculateWinner(squares) {
  const lines = [ // All winning possibilities in tic-tac-toe
    [0, 1, 2],
    [3, 4, 5],
    [6, 7, 8],
    [0, 3, 6],
    [1, 4, 7],
    [2, 5, 8],
    [0, 4, 8],
    [2, 4, 6]
  ];
  for (let i = 0; i < lines.length; i++) { // loop through lines array
    const [a, b, c] = lines[i]; // a,b,c, has to be an array and cannot be {}
    if (squares[a] && squares[a] === squares[b] && squares[a] === squares[c]) {
      return squares[a];
    }
  }
  return null;
}

export default calculateWinner;